    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            if (txt_username.getText().isEmpty() || txt_password.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "champs invalides");
            
            } else 
            {
                connexion con = new connexion();
                pst=con.con.prepareStatement("select * from utilisateur "
                        + "where nomutil=? and motdepasse=? ");
                pst.setString(1, txt_username.getText());
                pst.setString(2, txt_password.getText());
                rs=pst.executeQuery();
                
                if( !rs.isBeforeFirst()) { //donn√© existe ou pas
                    JOptionPane.showMessageDialog(this, "compte invalide");
                    txt_username.setText("");
                    txt_password.setText("");
                }
                else {
                    while (rs.next())
                    {
                        connection.super.dispose();
                        Menus fen = new Menus();
                        fen.setVisible(true);
                    }
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        } 
    }//GEN-LAST:event_jButton1ActionPerformed

